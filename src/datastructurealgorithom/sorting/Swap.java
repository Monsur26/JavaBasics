package datastructurealgorithom.sorting;

public class Swap {


    public static void main(String[] args) {


        int number1=50;
        int number2=60;

        int temp;

        // Swap / ReAssign variable value
        temp=number2; //
        System.out.println(temp); //60
        number1=number2;
        System.out.println(number1); // 60
        temp=number1;
        System.out.println(temp);//60
        number1=temp+number2;
        System.out.println(number1);//120
        temp=number1;
        System.out.println(temp);// 120






    }














}
