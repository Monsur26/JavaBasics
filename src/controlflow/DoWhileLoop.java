package controlflow;

public class DoWhileLoop {

// Post condition check:

    // Do while loop execute at least one time then it will check condition.
    // do{} while()

    public static void main(String[] args) {


        int age=20;

        do {
            System.out.println("Your age is "+age);
            age++;

        } while (age<28);




    }





}
